#How you can declare variables in python
number = 147
print(number)#Declare an int
print(type(number))

isTrue = True #Declare a boolean
print(isTrue)
print(type(isTrue))

floatValue = 147.00 #Declare a float value
print(floatValue)
print(type(floatValue))

myName = 'Rohan'  #Declare a string
print(myName)
print(type(myName))